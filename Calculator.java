	package week1.day2;

public class Calculator {

	public int add(int n1, int n2, int n3) {
		return n1+n2+n3;
	}
	public int diff(int n1, int n2) {
		return n1-n2;
	}
	public double mul(double n1, double n2) {
		return n1*n2;
	}
	public float div(float n1, float n2) {
		return n1/n2;
	}
	

	}

